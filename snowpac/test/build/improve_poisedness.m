delta = 1;
nodes = [0 0; ...
    0.9, 0.01; ...
    0.9, 0.005; ...
    -0.9, 0.1; ...
    -0.9, -.3];

figure(1); clf;
subplot(1,3,1);
ti = 0:0.01:2*pi+0.01;
plot( delta*cos(ti), delta*sin(ti), 'g-');
hold on;
for i = 1 : 1 : length(nodes(:,1))
    plot( nodes(i, 1), nodes(i, 2), 'r.', 'markersize', 30);
end
nodes = [0 0; ...
    0.9, 0.005; ...
    -0.9, 0.1; ...
    -0.9, -.3; ...
    0.50408, 0.86366];

for i = 1 : 1 : length(nodes(:,1))
    plot( nodes(i, 1), nodes(i, 2), 'b.', 'markersize', 20);
end
hold off;
axis([-2, 2, -2, 2]);
daspect([1 1 1]);
grid on;
title('test 1');
